    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String pass=txt_opass.getText().toString();
        String npass=txt_npass.getText().toString();
        String cpass=txt_cpass.getText().toString();
        if(pass.equals("")||npass.equals("")||cpass.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Fill All The Details");
        }
        else
        {
            if(npass.equals(cpass))
            {
            User U=new User();
            try {
                if( U.login(user, pass,"Student"))
                {
                   U. setData(user, npass);
                   JOptionPane.showMessageDialog(null,"Password Changed");
                   txt_opass.setText("");
                   txt_npass.setText("");
                   txt_cpass.setText("");

                }
                else
                {
                  JOptionPane.showMessageDialog(null,"Invalid Password");
                   txt_opass.setText("");
                   txt_npass.setText("");
                   txt_cpass.setText("");
                }} catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
        else
        {
             JOptionPane.showMessageDialog(null,"Password Does Not Match");
                   txt_npass.setText("");
                   txt_cpass.setText("");
        }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

