    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mail=null;
        String name=null;
        String con=null;
        String dob=null;
        String gender=null;
        String enroll=null;
        mail=txt_mail.getText().toString();
        name=txt_Name.getText().toString();
        con=txt_con.getText().toString();
        SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd");
        format.setLenient(false);
        dob=format.format(mydate.getDate());
        //dob=mydate.getDate().toString();
        JOptionPane.showMessageDialog(null,dob);
        gender=combo_gender.getSelectedItem().toString();
        enroll=txt_roll.getText().toString();
       if(mail.equals("")||name.equals("")||con.equals("")||dob.equals("")||gender.equals("")||enroll.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter All Details");
        }
        else
        {
            Student St=new Student();
            try {

                if( St.basicInfo(enroll, mail, con, dob, gender))
                {
                    JOptionPane.showMessageDialog(null,"Record Inserted");
                    txt_mail.setText("");
                    txt_Name.setText("");
                    txt_con.setText("");
                    txt_roll.setText("");
                    txt_hname.setText("");
                    txt_room.setText("");
                    jButton1.setEnabled(false);
                }
                else
                JOptionPane.showMessageDialog(null,"Record Not Inserted");

            } catch (SQLException ex) {
                System.err.println(ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

