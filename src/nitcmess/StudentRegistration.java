    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String mail=txt_roll.getText().toString();
        if(mail.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please Enter The Email First");
        }
        else
        {
            CheckUser CU=new CheckUser();
            try {
                ResultSet RS=CU.checkStudent(mail);
                if(RS!=null)
                {
                    txt_Name.setText(RS.getString("Name").toString());
                    txt_hname.setText(RS.getString("HostelName").toString());
                    txt_room.setText(RS.getString("RoomNo").toString());

                    jButton1.setEnabled(true);
                }
                else
                {
                    txt_roll.setText("");
                    JOptionPane.showMessageDialog(null,"Not Registered By Chief Warden");
                }
            } catch (SQLException ex) {
                System.err.println(ex);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

